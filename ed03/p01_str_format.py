def main():
    print("나는 %d살입니다." % 22)   # 나는 22살입니다.
    print("나는 %s을(를) 좋아합니다." % "파이썬")   # 나는 파이썬을(를) 좋아합니다.
    print("Apple은 %c로 시작합니다." %'A')  # Apple은 A로 시작합니다.
    print("나는 %s살입니다." % 22)  # 나는 22살입니다. -> %s는 정수값도 표현 가능!

    print("나는 %s색과 %s색을 좋아해요." % ("노란", "파란"))  # 나는 노란색과 파란색을 좋아해요.


main()
